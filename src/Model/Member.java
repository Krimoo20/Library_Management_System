package Model;

/**
 *
 * @author Abdelkarem Homidi
 */
public class Member {
    String ID;
    String name;
    String email;
    int mobileNumber;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(int mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public Member(String ID, String name, String email, int mobileNumber) {
        this.ID = ID;
        this.name = name;
        this.email = email;
        this.mobileNumber = mobileNumber;
    }
    
}
